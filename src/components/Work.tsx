import "./styles/Work.css";
import WorkImage from "./WorkImage";
import gsap from "gsap";
import { ScrollTrigger } from "gsap/ScrollTrigger";
import { useGSAP } from "@gsap/react";

gsap.registerPlugin(useGSAP);

const Work = () => {
  useGSAP(() => {
  let translateX: number = 0;

  function setTranslateX() {
    const box = document.getElementsByClassName("work-box");
    const rectLeft = document
      .querySelector(".work-container")!
      .getBoundingClientRect().left;
    const rect = box[0].getBoundingClientRect();
    const parentWidth = box[0].parentElement!.getBoundingClientRect().width;
    let padding: number =
      parseInt(window.getComputedStyle(box[0]).padding) / 2;
    translateX = rect.width * box.length - (rectLeft + parentWidth) + padding;
  }

  setTranslateX();

  let timeline = gsap.timeline({
    scrollTrigger: {
      trigger: ".work-section",
      start: "top top",
      end: `+=${translateX}`, // Use actual scroll width
      scrub: true,
      pin: true,
      id: "work",
    },
  });

  timeline.to(".work-flex", {
    x: -translateX,
    ease: "none",
  });

  // Clean up (optional, good practice)
  return () => {
    timeline.kill();
    ScrollTrigger.getById("work")?.kill();
  };
}, []);
  return (
    <div className="work-section" id="work">
      <div className="work-container section-container">
        <h2>
          My <span>Work</span>
        </h2>
        <div className="work-flex">
          {[
            {
              id: "01",
              name: "Healthcare Logistics",
              category: "2025",
              tools: "HTML/CSS, JavaScript",
              desc: "Created responsive pages for registration, login, and booking. Developed admin dashboard and implemented authentication.",
              image: "/Healthcare-logistics.jpg"
            },
            {
              id: "02",
              name: "Social-Media-Web-Application",
              category: "2025",
              tools: "React, Node.js, MongoDB",
              desc: "Built a React.js SPA with Context API. Designed scalable RESTful API with Node.js and Express to handle complex CRUD operations.",
              image: "/Social-media-web-application.jpg"
            },
            {
              id: "03",
              name: "Admin-Dashboard",
              category: "2026",
              tools: "React, Node.js, Express, MongoDB",
              desc: "Developed a responsive Admin Dashboard with interactive UI components. Designed structured navigation and analytics features.",
              image: "/Admin-dashboard.jpg"
            }
          ].map((project, index) => (
            <div className="work-box" key={index}>
              <div className="work-info">
                <div className="work-title">
                  <h3>{project.id}</h3>

                  <div>
                    <h4>{project.name}</h4>
                    <p>{project.category}</p>
                  </div>
                </div>
                <h4>Tools and features</h4>
                <p>{project.tools}</p>
              </div>
              <WorkImage image={project.image} alt={project.name} />
            </div>
          ))}
        </div>
      </div>
    </div>
  );
};

export default Work;
